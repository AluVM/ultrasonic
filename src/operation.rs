use amplify::confinement::SmallVec;
use amplify::Bytes32;

use crate::{CallId, Fiel128, OwnedState, StateData, StatePtr, LIB_NAME_ULTRASONIC};

#[cfg(feature = "chf-sha256")]
pub type Opid = Bytes32;
#[cfg(not(feature = "chf-sha256"))]
pub type Opid = Fiel128;

#[derive(Copy, Clone, PartialEq, Eq, PartialOrd, Ord, Hash, Debug)]
#[derive(StrictType, StrictDumb, StrictEncode, StrictDecode)]
#[strict_type(lib = LIB_NAME_ULTRASONIC)]
#[cfg_attr(feature = "serde", derive(Serialize, Deserialize), serde(rename_all = "camelCase"))]
pub struct OwnedInput {
    pub prev_state: StatePtr,
    pub witness: StateData,
}

#[derive(Clone, PartialEq, Eq, PartialOrd, Ord, Hash, Debug)]
#[derive(StrictType, StrictDumb, StrictEncode, StrictDecode)]
#[strict_type(lib = LIB_NAME_ULTRASONIC)]
#[cfg_attr(feature = "serde", derive(Serialize, Deserialize), serde(rename_all = "camelCase"))]
pub struct Operation {
    pub call_id: CallId,
    // TODO: Change to a map
    pub free_input: SmallVec<(Fiel128, StateData)>,
    pub owned_input: SmallVec<OwnedInput>,
}

#[derive(Clone, PartialEq, Eq, PartialOrd, Ord, Hash, Debug)]
#[derive(StrictType, StrictDumb, StrictEncode, StrictDecode)]
#[strict_type(lib = LIB_NAME_ULTRASONIC)]
#[cfg_attr(feature = "serde", derive(Serialize, Deserialize), serde(rename_all = "camelCase"))]
pub struct StateTransition {
    pub opid: Opid,
    // TODO: Change to a map
    pub free_state: SmallVec<(Fiel128, StateData)>, // TODO: add operation effect
    pub owned_state: SmallVec<OwnedState>,
}
